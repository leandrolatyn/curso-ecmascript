// Concatenacion de caracteres tradicional
let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Plantillas literales - Template literals
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Cadenas multilineas tradicionales
let lorem = 'Esto es un string \n' + 'Esto es otra linea';
console.log(lorem);

// Multilinea con plantillas literales
let lorem2 = `Esto es una frase epica
Esto es la continuacion de esa frase`;
console.log(lorem2);